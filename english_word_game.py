GRADES = {
    "1st": WORDS_1ST_GRADE,
    "2nd": WORDS_2ND_GRADE,
    "3rd": WORDS_3RD_GRADE,
}

ROUNDS_PER_GAME = 10


# --- Game Functions ---


def clear_screen():
    print("\n" + "=" * 60)


def display_header(difficulty=None, grade=None):
    print("=" * 60)
    print("      *** ENGLISH WORD ADVENTURE ***")
    print("      Synonym Quiz — Pick the Right Synonym!")
    print("=" * 60)
    if grade and difficulty:
        word_pool = get_word_pool(difficulty, grade)
        label = "all" if difficulty == "all" else difficulty
        print(f"  {grade.upper()} GRADE | Difficulty: {label.upper()} ({len(word_pool)} words)")
    print("  For each word, pick the correct synonym from 4 choices.")
    print("=" * 60)


def get_word_list(grade):
    """Get the word list for a grade level."""
    return GRADES.get(grade, WORDS_2ND_GRADE)


def get_word_pool(difficulty, grade="2nd"):
    """Get words filtered by difficulty within a grade level."""
    words = get_word_list(grade)
    if difficulty == "all":
        return list(words)
    return [w for w in words if w["difficulty"] == difficulty]


def choose_grade():
    print("\n  Choose your grade level:")
    print(f"  [1] 1st Grade ({len(WORDS_1ST_GRADE)} words)")
    print(f"  [2] 2nd Grade ({len(WORDS_2ND_GRADE)} words)")

    while True:
        try:
            choice = int(input("  > ").strip())
            if choice == 1:
                return "1st"
            elif choice == 2:
                return "2nd"
            else:
                print("  Please enter 1 or 2.")
        except ValueError:
            print("  Please enter a valid number.")


def choose_difficulty(grade="2nd"):
    words = get_word_list(grade)
    easy_count = len([w for w in words if w["difficulty"] == "easy"])
    med_count = len([w for w in words if w["difficulty"] == "medium"])
    hard_count = len([w for w in words if w["difficulty"] == "hard"])

    print("\n  Choose your difficulty level:")
    print(f"  [1] Easy   ({easy_count} words) — common, short words")
    if med_count > 0:
        print(f"  [2] Medium ({med_count} words) — longer words with tricky spelling")
    if hard_count > 0:
        print(f"  [3] Hard   ({hard_count} words) — challenging vocabulary")
    print(f"  [4] All    ({len(words)} words) — the full word list")

    while True:
        try:
            choice = int(input("  > ").strip())
            if choice == 1:
                return "easy"
            elif choice == 2 and med_count > 0:
                return "medium"
            elif choice == 3 and hard_count > 0:
                return "hard"
            elif choice == 4:
                return "all"
            else:
                print("  Please enter a valid option.")
        except ValueError:
            print("  Please enter a valid number.")


def get_yes_no(prompt):
    while True:
        answer = input(prompt).strip().lower()
        if answer in ("yes", "y"):
            return True
        elif answer in ("no", "n"):
            return False
        else:
            print("      Please answer 'yes' or 'no'.")


def build_choices(word):
    """Build 4 shuffled choices: 1 correct synonym + 3 distractors."""
    correct = random.choice(word["synonyms"])
    wrong = random.sample(word["distractors"], 3)
    choices = [correct] + wrong
    random.shuffle(choices)
    return choices, correct


def build_antonym_choices(word):
    """Build 4 shuffled choices: 1 correct antonym + 3 antonym distractors."""
    correct = random.choice(word["antonyms"])
    wrong = random.sample(word["antonym_distractors"], 3)
    choices = [correct] + wrong
    random.shuffle(choices)
    return choices, correct


VOWELS = set("aeiou")
CONSONANTS = set("bcdfghjklmnpqrstvwxyz")


def build_missing_letter_choices(word):
    """Build a missing-letter puzzle: blank one letter, offer 4 letter choices.

    Returns (blanked_word, choices, correct_letter, blank_index).
    """
    name = word["name"].lower()
    # Pick a random letter index (skip spaces/hyphens)
    letter_indices = [i for i, ch in enumerate(name) if ch.isalpha()]
    blank_idx = random.choice(letter_indices)
    correct = name[blank_idx]

    # Build blanked display: e.g. "h _ p p y"
    blanked = name[:blank_idx] + "_" + name[blank_idx + 1:]

    # Pick 3 wrong letters from the same group (vowel or consonant)
    if correct in VOWELS:
        pool = VOWELS - {correct}
    else:
        pool = CONSONANTS - {correct}
    wrong = random.sample(sorted(pool), 3)

    choices = [correct] + wrong
    random.shuffle(choices)
    return blanked, choices, correct, blank_idx


def play_round(word, round_num, total_rounds):
    """Play one round: show word + definition, pick synonym from 4 choices."""
    choices, correct = build_choices(word)

    print(f"\n  --- Round {round_num} of {total_rounds} ---")
    print(f"\n  Word: {word['name'].upper()}")
    print(f"  \"{word['definition']}\"")
    print(f"\n  Which is a SYNONYM of '{word['name']}'?\n")

    for i, choice in enumerate(choices):
        print(f"  [{i + 1}] {choice}")

    while True:
        try:
            pick = int(input("\n  Your answer (1-4): ").strip())
            if 1 <= pick <= 4:
                break
            else:
                print("  Please enter 1, 2, 3, or 4.")
        except ValueError:
            print("  Please enter a valid number.")

    chosen = choices[pick - 1]
    is_correct = chosen == correct

    if is_correct:
        print(f"\n  *** CORRECT! *** '{chosen}' is a synonym of '{word['name']}'.")
    else:
        print(f"\n  Not quite! You picked '{chosen}'.")
        print(f"  The correct answer was: '{correct}'")

    return is_correct


def show_word_facts(word):
    """Show educational info about the word after a round."""
    print(f"\n  --- LEARN ABOUT THIS WORD ---")
    print(f"  Definition: {word['definition']}")
    print(f"  Example: {word['example']}")
    print(f"  All synonyms: {', '.join(word['synonyms'])}")
    print(f"\n  Learning tip: {word['tip']}")


def show_session_review(words_seen):
    print("\n" + "=" * 60)
    print("      VOCABULARY REVIEW — Words You Learned Today")
    print("=" * 60)

    for result, word in words_seen:
        icon = "+" if result else "-"
        print(f"\n  [{icon}] {word['name']} ({word['difficulty']})")
        print(f"      {word['definition']}")
        print(f"      Synonyms: {', '.join(word['synonyms'])}")
        print(f"      Tip: {word['tip']}")

    correct_words = [w["name"] for r, w in words_seen if r]
    missed = [w["name"] for r, w in words_seen if not r]

    print("\n" + "-" * 60)
    if correct_words:
        print(f"  Correct: {', '.join(correct_words)}")
    if missed:
        print(f"  Review:  {', '.join(missed)}")
    print("=" * 60)


def main():
    display_header()
    grade = choose_grade()
    difficulty = choose_difficulty(grade)
    word_pool = get_word_pool(difficulty, grade)
    total_rounds = min(ROUNDS_PER_GAME, len(word_pool))

    clear_screen()
    display_header(difficulty, grade)

    wins = 0
    streak = 0
    best_streak = 0
    words_seen = []

    # Pick words for this session (no repeats)
    session_words = random.sample(word_pool, total_rounds)

    for round_num, word in enumerate(session_words, 1):
        correct = play_round(word, round_num, total_rounds)
        show_word_facts(word)
        words_seen.append((correct, word))

        if correct:
            wins += 1
            streak += 1
            if streak > best_streak:
                best_streak = streak
            if streak == 3:
                print("\n  *** 3 in a row! You're on fire! ***")
            elif streak == 5:
                print("\n  *** 5 in a row! Word master! ***")
            elif streak >= 7:
                print(f"\n  *** {streak} in a row! Unstoppable! ***")
        else:
            if streak >= 3:
                print(f"\n  Streak ended at {streak}. Great run!")
            streak = 0

        print(f"\n  Score: {wins}/{round_num}")
        if streak >= 2:
            print(f"  Current streak: {streak} | Best streak: {best_streak}")

        if round_num < total_rounds:
            input("\n  Press Enter for the next word...")
            clear_screen()

    # End of session
    show_session_review(words_seen)
    print(f"\n  Thanks for playing English Word Adventure!")
    print(f"  Final score: {wins} out of {total_rounds}")
    if best_streak >= 2:
        print(f"  Best win streak: {best_streak}")
    print("  Keep learning new words every day!")
    print("=" * 60)

    print()
    play_again = get_yes_no("  Play again? (yes/no): ")
    if play_again:
        clear_screen()
        main()


if __name__ == "__main__":
    main()
